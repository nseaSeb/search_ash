defmodule SearchAsh.Preparations.Search do
  @moduledoc """
  Preparation generated by `SearchAsh` for the `:search` read action. Builds a
  tsquery from the `query` + `language` arguments (same `SearchCore` pipeline used at
  index time) and filters on the `'simple'` tsvector of the search-text column.

  A blank/absent `query` lists all rows (unranked); a non-blank one whose tokens are
  all eliminated (stopwords, too short) returns no rows.
  """
  use Ash.Resource.Preparation
  require Ash.Query

  @impl true
  def prepare(query, _opts, _context) do
    resource = query.resource
    term = Ash.Query.get_argument(query, :query)

    # Blank/absent query → no filter, so a list UI shows all rows before you type.
    # A non-blank term whose tokens are ALL eliminated (stopwords, < min_length: "de")
    # returns nothing instead — the user searched for something unsearchable, and
    # pre-0.4.0 silently listing the whole base was a bug.
    if blank?(term) do
      query
    else
      language = normalize_language(Ash.Query.get_argument(query, :language), resource)

      tsquery =
        SearchCore.tsquery(term, language,
          prefix: SearchAsh.Info.prefix?(resource),
          synonyms: SearchAsh.Info.synonyms(resource, language)
        )

      if tsquery == "" do
        Ash.Query.filter(query, false)
      else
        query
        |> filter(SearchAsh.Info.search_text_attribute(resource), tsquery)
        |> apply_rank(SearchAsh.Info.rank?(resource), tsquery)
      end
    end
  end

  defp blank?(term), do: not is_binary(term) or String.trim(term) == ""

  defp filter(query, search_text_attribute, tsquery) do
    Ash.Query.filter(
      query,
      fragment(
        "?::tsvector @@ to_tsquery('simple', ?)",
        ^ref(search_text_attribute),
        ^tsquery
      )
    )
  end

  # Rank and expose `:search_rank` only when there is an actual query. A blank query
  # (list-all) is returned unranked and does NOT load `:search_rank` — check
  # `Ash.Resource.loaded?(row, :search_rank)` if you display it.
  defp apply_rank(query, false, _tsquery), do: query

  defp apply_rank(query, true, tsquery) do
    query
    |> Ash.Query.load(search_rank: %{tsquery: tsquery})
    |> Ash.Query.sort(search_rank: {%{tsquery: tsquery}, :desc})
    |> stable_order()
  end

  # Break ties on the primary key so equally-ranked rows keep a deterministic order
  # (stable pagination). Appended after the rank sort.
  defp stable_order(query) do
    case Ash.Resource.Info.primary_key(query.resource) do
      [] -> query
      pkey -> Ash.Query.sort(query, Enum.map(pkey, &{&1, :asc}))
    end
  end

  # Fall back to the resource's default language for anything that isn't a supported
  # language (nil, a non-atom, or an unknown atom) — never crash the stemmer.
  defp normalize_language(lang, resource) do
    if is_atom(lang) and SearchCore.Language.supported?(lang) do
      lang
    else
      SearchAsh.Info.default_language(resource)
    end
  end
end
