defmodule SearchAsh.Preparations.Search do
  @moduledoc """
  Preparation generated by `SearchAsh` for the `:search` read action. Builds a
  tsquery from the `query` + `language` arguments (same `SearchCore` pipeline used at
  index time) and filters on the `'simple'` tsvector of the search-text column.
  """
  use Ash.Resource.Preparation
  require Ash.Query

  @impl true
  def prepare(query, _opts, _context) do
    resource = query.resource
    search_text_attribute = SearchAsh.Info.search_text_attribute(resource)
    term = Ash.Query.get_argument(query, :query)
    language = normalize_language(Ash.Query.get_argument(query, :language), resource)
    # Branch on the *computed* tsquery: a short (< min_length) or all-stopwords query
    # yields no tokens ("") even when the term is non-blank.
    tsquery =
      if is_binary(term),
        do: SearchCore.tsquery(term, language, prefix: SearchAsh.Info.prefix?(resource)),
        else: ""

    if tsquery == "" do
      # Nothing searchable → no filter, so a list UI shows all rows before you type.
      query
    else
      query
      |> Ash.Query.filter(
        fragment(
          "to_tsvector('simple', ?) @@ to_tsquery('simple', ?)",
          ^ref(search_text_attribute),
          ^tsquery
        )
      )
      |> maybe_rank(SearchAsh.Info.rank?(resource), tsquery)
    end
  end

  # Order by relevance and expose the score, unless ranking is disabled.
  defp maybe_rank(query, false, _tsquery), do: query

  defp maybe_rank(query, true, tsquery) do
    query
    |> Ash.Query.load(search_rank: %{tsquery: tsquery})
    |> Ash.Query.sort(search_rank: {%{tsquery: tsquery}, :desc})
  end

  defp normalize_language(lang, _resource) when is_atom(lang) and not is_nil(lang), do: lang
  defp normalize_language(_lang, resource), do: SearchAsh.Info.default_language(resource)
end
